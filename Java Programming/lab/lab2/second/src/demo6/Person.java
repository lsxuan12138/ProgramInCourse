package second.src.demo6;

public class Person {
    private int money;
    private int age;

    public Person(int money, int age) {
        this.money = money;
        this.age = age;
    }

    public int getMoney() {
        return money;
    }

    public void setMoney(int money) {
        this.money = money;
    }

    public int getAge() {
        return age;
    }

    public void setAge(int age) {
        this.age = age;
    }
}
